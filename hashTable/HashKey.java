package hashPackage;

public class HashKey {
       public Object key;
       public Object value;
       public HashKey next;
       
       public HashKey () {
    	   this.key = key;
    	   this.value = 0;
    	   this.next = null;
    	   
       }
       
       public HashKey(Object key, Object value)
       {
    	   this.key = key;
    	   this.value = value;
    	   this.next = next;
       }
}
