package nodeHomework;

public class NodeMainClass {
	
	public int value;
	public NodeMainClass next;

	
	public NodeMainClass() {
		this.value = 0;
		this.next = null;
	}
	
	public NodeMainClass (int value)
	{
		this.value = value;
		this.next = next;
	}
	@Override
	public String toString ()
	{
		return " " + this.value;
	}
	public int getValue()
	{
		return this.value;
	}
}


